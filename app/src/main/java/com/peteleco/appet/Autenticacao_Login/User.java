package com.peteleco.appet.Autenticacao_Login;

public class User {
    public String nome;
    public String email;
    public String cpf;
    public String telefone;
    public String grr;


    public User(String nome, String email, String cpf, String telefone, String grr) {
        this.nome = nome;
        this.email = email;
        this.cpf = cpf;
        this.telefone = telefone;
        this.grr = grr;
    }
}
